//    random Map variant
function randomMap (lev, levLength) {
	var levelMap=['ss00000000ss',
								'ss00000000ss',
								'ss00000000ss',
								'ss00000000ss',
								'ss00000000ss',
								'ss00000000ss',
								'ss00000000ss',
								'ss00000000ss',
								'ss00000000ss'
								]; // map
	levLength = levLength<100?100:levLength; // min 100
	// level check
	var minWindow;
	switch (lev) {
		case 0:
			minWindow = '00000000';
			break;
		case 1:
			minWindow = '0000000';
			break;
		case 2:
			minWindow = '000000';
			break;
		case 3:
			minWindow = '00000';
			break;
		case 4:
			minWindow = '0000';
			break;
	}
	//  build level
	for (var i=10; i<levLength; i++) {
		var str='';
		for (var j=0; j<12; j++) {
			str += Math.round(Math.random())==0?'s':'0';
		}
		// insert min window
		var rand = Math.round(Math.random()*(minWindow.length-4));
		str = str.slice(0,rand) + minWindow + str.slice(rand+minWindow.length);
		// insert enemy
		var rand = Math.round(Math.random()*(j=2));
		str = Math.round(Math.random()+0.3)==0?str.slice(0,rand) + 'e' + str.slice(rand+2):str;
		levelMap.push (str);
	}
return levelMap;
}

